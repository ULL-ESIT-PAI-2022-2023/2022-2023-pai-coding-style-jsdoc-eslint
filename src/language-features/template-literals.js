function arithmetic(operating1, operating2) {
  return `Here is a table of arithmetic operations:
${operating1} + ${operating2} = ${operating1 + operating2}
${operating1} - ${operating2} = ${operating1 - operating2}
${operating1} * ${operating2} = ${operating1 * operating2}
${operating1} / ${operating2} = ${operating1 / operating2}`;
}